RESEARCH_NOTE = """[검색 및 검증 기록]
2026-06-11에 유물 제목, 귀면와·용보·노리개·나전필통·십이지 해시계 등의 용어로 공식 박물관 및 문화유산 자료 검색을 시도하였다. 현재 검색 결과에서 이 전시물들의 개별 소장품 페이지를 확정하지 못했으므로, 수정본의 보충 설명은 사진 속 전시문과 일반적으로 확인되는 유물 용어의 범위에 한정하였다. 소장처, 지정번호, 정확한 제작 연대처럼 사진에 없는 정보는 새로 단정하지 않았다.
"""


def corrected_name(path: Path) -> Path:
    return path.with_name(f"{path.stem}(수정본){path.suffix}")


def main() -> None:
    originals = sorted(
        p for p in SOURCE_DIR.glob("*.txt") if not p.stem.endswith("(수정본)")
    )
    for path in originals:
        text = path.read_text(encoding="utf-8")
        for before, after in REPLACEMENTS.get(path.name, []):
            text = text.replace(before, after)
        supplement = SUPPLEMENTS[path.name]
        text = (
            text.rstrip()
            + "\n\n[교정 및 보충 설명]\n"
            + supplement
            + "\n\n"
            + RESEARCH_NOTE
        )
        corrected_name(path).write_text(text, encoding="utf-8")
    print(f"Wrote {len(originals)} corrected transcript files")


if __name__ == "__main__":
    main()
